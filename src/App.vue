<template>
  <div className="app">
    <header className="navbar"><h1>Movies</h1></header>
    <div className="main">
      <Search :genres="genres" :showFaves="showFaves" />
      <Movies :movies="movies" />
    </div>
  </div>
</template>

<script>
import Search from "./components/Search";
import Movies from "./components/Movies";
import axios from "axios";
export default {
  name: "App",
  data: function () {
    return {
      genres: [],
      movies: [],
      showFaves: false,
    };
  },
  components: {
    Search,
    Movies,
  },
  computed: {
    genresComputed() {
      return this.genres;
    },
    moviesComputed() {
      return this.movies;
    },
  },
  created: function () {
    axios
      .get("./genres")
      .then(({ data }) => {
        console.log(data);
        console.log("this is the genres property from data: ", data.genres);
        this.genres = data.genres;
      })
      .catch((err) => console.log(err))
      .then(() =>
        axios
          .get("./movies", {
            params: {
              with_genres: 28,
            },
          })
          .then(({ data }) => {
            this.movies = data.results;
          })
          .catch((err) => console.log(err))
      );
  },
};
</script>

